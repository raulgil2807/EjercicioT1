package com.ejercicio1;

public class Ejercicio1 {

    public static void main(String[] args) {

        //1 - Datos Num√©ricos

        // 1.1 Tipo de datos: Enteros.

        byte variable1 = 10;
        short variable2 = 5;
        int variable3 = 20;
        long variable4 = 7;

        System.out.println(variable1 + " " + variable2 + " " + variable3 + "" + " " + variable4);

        // 1.2 Tipos de datos: Decimales.

        float variable5 = 1.0f;
        double variable6 = 10.4d;

        System.out.println(variable5 + " " + variable6);

        // 2. Tipos de datos: Booleanos.

        boolean variable7 = true;
        boolean variable8 = false;

        System.out.println(variable7 + " " + variable8);

        // 3. Tipos de datos: Texto.

        char variable9 = 'a';
        String variable10 = "Raul";

        System.out.println(variable9 + " " + variable10);
    }
}
